/*
  - O que é controle de fluxo?
  --------------------------------------------------
  É uma maneira de definir como e quando determinado
  tipo de código será executado.

  Podemos fazer determinados tipos de verificações,
  utilizando estruturas condicionais. Para isso, vamos
  trabalhar bastante com o conceito de 'loops' e 'con-
  ditional statements'

*/