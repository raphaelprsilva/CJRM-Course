/*---------------------------------------------------
  Funções e Métodos
---------------------------------------------------*/

// Método = Função
const name = 'Raphael';

// Funções
const sayHi = () => 'Oi!';

const greet = sayHi();

console.log(greet);


// Métodos -> são funções que fazem alguma ação
// São diferentes na maneira como são criados e invocados
const nameInUpperCase = name.toUpperCase();

console.log(nameInUpperCase);